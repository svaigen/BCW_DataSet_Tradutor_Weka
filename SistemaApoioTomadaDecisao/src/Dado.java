public class Dado {
    private int intt;
    private double floatt;
    private String str;

    public Dado(int intt) {
        this.intt = intt;
    }

    public Dado(double floatt) {
        this.floatt = floatt;
    }

    public Dado(String str) {
        this.str = str;
    }

    public int getInt() {
        return intt;
    }

    public double getFloat() {
        return floatt;
    }

    public String getStr() {
        return str;
    }
    
    
}
